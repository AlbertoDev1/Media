# Calculate the average of 4 numbers

def average(num1, num2, num3, num4):
  # Calculate the sum
  sum = num1 + num2 + num3 + num4

  # Calculate the average
  avg = sum / 5

  # Return the average
  return avg

# Call the function with four numbers
result = average(1, 2, 3, 4, 5)

# Print the result
print(result) 